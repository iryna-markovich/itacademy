package it.academy;

public class Employee {
    private String name;
    private String surname;
    private String position;
    private String recruitment;
    private int salary;

    public Employee(String name, String surname, String position, String recruitment, int salary) {
        this.name = name;
        this.surname = surname;
        this.position = position;
        this.recruitment = recruitment;
        this.salary = salary;
    }
}
